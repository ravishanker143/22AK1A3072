# Read two 10-digit integers
num1 = int(input("Enter a 10-digit integer: "))
num2 = int(input("Enter another 10-digit integer: "))

# Demonstrate relational operations
print("Relational Operations:")
print("num1 =", num1)
print("num2 =", num2)

print("num1 == num2:", num1 == num2)
print("num1 != num2:", num1 != num2)
print("num1 > num2:", num1 > num2)
print("num1 < num2:", num1 < num2)
print("num1 >= num2:", num1 >= num2)
print("num1 <= num2:", num1 <= num2)